package teacher;

public class Students {
	String stuName;
	String stuID;
	String stuSub;
	String stuClass;
	
	public Students() {}
	
	public Students(String stuName,String stuID,String stuSub,String stuClass) {
		this.stuName=stuName;
		this.stuID=stuID;
		this.stuSub=stuSub;
		this.stuClass=stuClass;
	}
	
	
}
